package Week12;

public class Four_arithmetic_operation {
	public Four_arithmetic_operation() {
		System.out.println("Four arithmetic_operation");
	}
	
	public void addition (double val1, double val2) {
		System.out.println(val1+val2);
	}
	
	public void subtraction (double val1, double val2) {
		System.out.println(val1-val2);
	}
	
	public void multiplication(double val1, double val2) {
		System.out.println(val1*val2);
	}
	
	public void division (double val1, double val2) {
		System.out.println(val1/val2);
	}
	
}
